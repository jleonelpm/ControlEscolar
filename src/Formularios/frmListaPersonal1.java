private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
 // TODO add your handling code here:
    int filas=this.jTable1.rowAtPoint(evt.getPoint());

    if(filas>-1)
    {
        String p;        
        p = String.valueOf(jTable1.getValueAt(filas, 0)) + "," + String.valueOf(jTable1.getValueAt(filas, 2)) + " " + String.valueOf(jTable1.getValueAt(filas, 3) + " " + String.valueOf(jTable1.getValueAt(filas, 4)));
        frmCargaAcademicaDocentes.docente = p;
        this.dispose();
    }
}//GEN-LAST:event_jTable1MouseClicked

