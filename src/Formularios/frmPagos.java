    private void txtMatriculaKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtMatriculaKeyReleased
        // TODO add your handling code here:
        if ((evt.getKeyCode() == KeyEvent.VK_ENTER)) {
            try {

                ctrlAlumnos ctrlalumno = new ctrlAlumnos();
                ArrayList<Alumnos> lista = null;
                Alumnos objalumno;
                //Se modificó el procedimiento almacenado, agregándose un criterio de selección
                //Realizado por leonel el 7 de abril de 2012
                lista = ctrlalumno.getFiltroPersonal("MatriculaUno", this.txtMatricula.getText());
                if (lista.size() > 0) {
                    this.txtIdAlumno.setText("");
                    this.lblNombre.setText("");
                    this.lblApellidoPaterno.setText("");
                    this.lblApellidoMaterno.setText("");
                    objalumno = lista.get(0);
                    this.txtIdAlumno.setText(String.valueOf(objalumno.getIdAlumno()));
                    this.lblNombre.setText(objalumno.getNombre());
                    this.lblApellidoPaterno.setText(objalumno.getApePaterno());
                    this.lblApellidoMaterno.setText(objalumno.getApeMaterno());
                } else {
                    this.txtIdAlumno.setText("");
                    this.lblNombre.setText("");
                    this.lblApellidoPaterno.setText("");
                    this.lblApellidoMaterno.setText("");
                }
            } catch (SQLException ex) {
                Logger.getLogger(frmPagos.class.getName()).log(Level.SEVERE, null, ex);
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(frmPagos.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_txtMatriculaKeyReleased

    
   
