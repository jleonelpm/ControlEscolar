    private void agregarPartida() {
        if (tabla.getRowCount() < 5) {
            p = null;
            frmPartidas1 frmpartidas = new frmPartidas1(this, true);
            frmpartidas.setLocationRelativeTo(this);
            frmpartidas.setVisible(true);
            if (p != null) {
                m = (DefaultTableModel) tabla.getModel();
                String partida[] = new String[6];
                partida[0] = p.getIdCuenta();
                partida[1] = p.getDescripcion();
                partida[2] = String.valueOf(p.getImporte());
                int cantidad = 1;
                partida[3] = String.valueOf(cantidad);
                float subtotal = p.getImporte() * cantidad;
                partida[4] = String.valueOf(subtotal);
                m.addRow(partida);
                formatTotal();
            } else {
                p = null;
            }
        }
    }

    private void quitarPartida() {
        int fila = tabla.getSelectedRow();
        if (fila > -1) {
            if (JOptionPane.showConfirmDialog(this, "¿Deseas eliminar esta partida?") == JOptionPane.YES_OPTION) {
                m = (DefaultTableModel) tabla.getModel();
                m.removeRow(fila);
                formatTotal();
            }
        }
    }

    private void formatTotal() {
        NumberFormat formatter = new DecimalFormat("#,##0.00");
        lblTotal.setText(formatter.format(getTotal()));
    }

    private float getTotal() {
        float total = 0;
        for (int i = 0; i <= tabla.getRowCount() - 1; i++) {
            total += Float.parseFloat(tabla.getValueAt(i, 4).toString());
        }
        return total;
    }

    private void guardar() {
        try {
            if (validar()) {
                if (JOptionPane.showConfirmDialog(this, "¿Deseas guardar los cambios?") == JOptionPane.YES_OPTION) {
                    pago.setIdPago(0);
                    String fecha = String.valueOf(dpFecha.getEditor().getText());
                    pago.setFecha(fecha);
                    pago.setIdAlumno(Integer.parseInt(txtIdAlumno.getText()));
                    pago.setIdUsuario(frmPrincipal.usuarioActual.getId_usuario());
                    pago.setImporteTotal(getTotal());
                    pago.setObservaciones("");
                    imprimirRecibo();
                    this.dispose();
                    boolean result = cpago.setPago(pago, tabla.getModel());                    

                    
                    if (result) {
                        
                        JOptionPane.showMessageDialog(this, "Los cambios fueron realizados exitosamente");
                        this.setVisible(result);
                        this.dispose();
                    } else {
                        JOptionPane.showMessageDialog(this, "El pago no pudo agregarse debido a un problema");
                    }
                }
            }
        } catch (SQLException ex) {
            Logger.getLogger(frmPagos.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(frmPagos.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void imprimirRecibo() {
        
        long lnMilisegundos = dpFecha.getDate().getTime();
        java.sql.Date fecha = new java.sql.Date(lnMilisegundos);
        String[] f = fecha.toString().split("-");
        
        Recibo recibo=null;

        recibo.setFolio(0); //El folio será capturado por el cajero
        
        recibo.setAnio(Integer.parseInt(f[0]));
        recibo.setMes(Integer.parseInt(f[1]));
        recibo.setDia(Integer.parseInt(f[2])); 
        recibo.setEntidadFederativa("Yucatán");
        recibo.setRfc("0000000000000");
        
        recibo.setMatricula(txtMatricula.getText());
        recibo.setNombre(lblNombre.getText());
        recibo.setApepat(lblApellidoPaterno.getText());
        recibo.setApemat(lblApellidoMaterno.getText());
        recibo.setDomicilio(lblDomicilio.getText());
        
        
        
        
        
        
        
    }

    private boolean validar() {
        boolean result = false;


        if ("".equals(this.txtIdAlumno.getText())) {
            JOptionPane.showMessageDialog(this, "Escribe la matricula del alumno");
        } else if (m.getRowCount() <= 0) {
            JOptionPane.showMessageDialog(this, "No has agregardo ninguna partida");
        } else {
            result = true;
        }

        return result;

    }

