  private  void guardar(){
    try {
    if (validar() ) {
    
      if (JOptionPane.showConfirmDialog(this, "¿Deseas guardar los cambios?") == JOptionPane.YES_OPTION) {

        if ("".equals(lblIdCiclo.getText())) {
          ce.setIdCiclo(0);      
          ce.setActivo(null); //Todos los ciclos cuando se crean son nulos          
        }else{
          ce.setIdCiclo(Integer.parseInt(lblIdCiclo.getText()));                      
        }

        ce.setPeriodo(txtPeriodo.getText());
        ce.setTipo((short)(cmbTipo.getSelectedIndex()+ 1));

        String fecha_inicial = String.valueOf(dpFechaInicio.getEditor().getText());
        String fecha_final = String.valueOf(dpFechaFin.getEditor().getText());     
        String fecha_parcial1 = String.valueOf(dpFechaParcial1.getEditor().getText());
        String fecha_parcial2 = String.valueOf(dpFechaParcial2.getEditor().getText());        
        String fecha_parcial3 = String.valueOf(dpFechaParcial3.getEditor().getText());                
        ce.setFechaInicial(fecha_inicial); //se debe enviar como año/mes/dia
        ce.setFechaFinal(fecha_final); //se debe enviar como año/mes/dia
        //Estos datos están pendientes por modificar

        ce.setFechaParcial1(fecha_parcial1); //se debe enviar como año/mes/dia
        ce.setFechaParcial2(fecha_parcial2); //se debe enviar como año/mes/dia
        ce.setFechaParcial3(fecha_parcial3); //se debe enviar como año/mes/dia        
        
        int result = ciclo.guardarCiclo(ce);

            if (result > 0) {
             updateTabla();
             limpiar();
              JOptionPane.showMessageDialog(this, "Los cambios fueron realizados con éxito");             
            }else{
              JOptionPane.showMessageDialog(this, "El ciclo escolar no pudo agregarse debido a un problema");                    
            }// fin de if (result > 0)     
          } //fin de ¿Guardar los cambios?
        } //fin de validar
    }catch (Exception e){
     JOptionPane.showMessageDialog(this, "error" + e.getMessage());                
    }
  }
  
  private void eliminar(){
    try {    
      if (!"".equals(lblIdCiclo.getText())) {
      if (JOptionPane.showConfirmDialog(this, "¿Deseas eliminar esta ciclo escolar?") == JOptionPane.YES_OPTION) {
        int id_ciclo = Integer.parseInt(lblIdCiclo.getText());
        int result = ciclo.deleteCiclo(id_ciclo);
        if (result > 0) {
          updateTabla();
          limpiar();
          JOptionPane.showMessageDialog(this, "El ciclo escolar fue eliminado con éxito");             
        }else{
          JOptionPane.showMessageDialog(this, "El ciclo escolar no pudo eliminarse debido a un problema");                    
        }
      }
      }else{
          JOptionPane.showMessageDialog(this, "Selecciona un ciclo escolar");                     
      }
    }catch (Exception e) {
      JOptionPane.showMessageDialog(this, e.getMessage());                          
    }
    
  }
  
