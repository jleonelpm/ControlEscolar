private void tablaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaMouseClicked
// TODO add your handling code here:
        fila = tabla.rowAtPoint(evt.getPoint());                 
           if (fila > -1){                                      
      try {
        lblIdCiclo.setText(String.valueOf(tabla.getValueAt(fila, 0)));           
        txtPeriodo.setText(String.valueOf(tabla.getValueAt(fila, 1))); 
        int i = Short.parseShort(String.valueOf(tabla.getValueAt(fila, 2)));
        cmbTipo.setSelectedIndex((short)(i-1));
        
        String f[];
        f = tabla.getValueAt(fila, 3).toString().split("-");
        fechaInicial = formato.parse(f[2]+"-"+f[1]+"-"+f[0]);
        f = tabla.getValueAt(fila, 4).toString().split("-");
        fechaFinal = formato.parse(f[2]+"-"+f[1]+"-"+f[0]);
        dpFechaInicio.setDate(fechaInicial);
        dpFechaFin.setDate(fechaFinal);
        
        f = tabla.getValueAt(fila, 6).toString().split("-");
        fechaParcial1 = formato.parse(f[2]+"-"+f[1]+"-"+f[0]);
        
        f = tabla.getValueAt(fila, 7).toString().split("-");
        fechaParcial2 = formato.parse(f[2]+"-"+f[1]+"-"+f[0]);

        f = tabla.getValueAt(fila, 8).toString().split("-");
        fechaParcial3 = formato.parse(f[2]+"-"+f[1]+"-"+f[0]);
        
        dpFechaParcial1.setDate(fechaParcial1);        
        dpFechaParcial2.setDate(fechaParcial2);        
        dpFechaParcial3.setDate(fechaParcial3);       
        
      } catch (ParseException ex) {
        Logger.getLogger(frmCiclos.class.getName()).log(Level.SEVERE, null, ex);
      }
         }
}//GEN-LAST:event_tablaMouseClicked

