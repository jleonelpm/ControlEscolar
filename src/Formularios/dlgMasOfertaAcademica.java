private void getMasOfertaAcademica()
{
        try {
            ctrlOfertaAcademica ctrloferta=new ctrlOfertaAcademica();
            ArrayList<OfertaAcademica> listaoferta=ctrloferta.getMasOfertaAcademica(al.getIdAlumno());
            for(int i=0;i<listaoferta.size();i++)
            {
                OfertaAcademica e=listaoferta.get(i);
                e.setSeleccionado(false);
             listaoferta.set(i, e);
            }
            ModeloOfertaAsignaturas modelo=ctrloferta.getOfertaInscripcion(listaoferta);
            this.jTable1.setModel(modelo);
        } catch (SQLException ex) {
            Logger.getLogger(dlgMasOfertaAcademica.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(dlgMasOfertaAcademica.class.getName()).log(Level.SEVERE, null, ex);
        }
 
}
