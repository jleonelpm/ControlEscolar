private ArrayList<Inscripciones> getInscripcionAjuste()
{ 
     ArrayList<Inscripciones> listaAjuste=new ArrayList<Inscripciones>();
        try {
           
            ModeloOfertaAsignaturas modelo=(ModeloOfertaAsignaturas) this.jTable1.getModel();
            ArrayList<OfertaAcademica> lista= modelo.getListaOfertaAcademica();
            ctrlCicloEscolar ctrlciclo=new ctrlCicloEscolar();
            CicloEscolar activo=ctrlciclo.getCicloActivo();
            
            
                      for(int i=0;i<lista.size();i++)
                      {
                          if(lista.get(i).isSeleccionado())
                          {
                              Inscripciones ajuste=new Inscripciones();
                              ajuste.setId_inscripcion(0);
                              ajuste.setId_alumno(al.getIdAlumno());
                              ajuste.setId_oferta_academica(lista.get(i).getIdOfertaAcademica());
                              ajuste.setId_ciclo(activo.getIdCiclo());
                              ajuste.setRecursamiento("N");
                              ajuste.setModulo(lista.get(i).getAsignatura().getModulo());
                              ajuste.setId_asignatura(lista.get(i).getIdAsignatura());
                              listaAjuste.add(ajuste);
                              
                              
                          }
                      }
                      
        } catch (SQLException ex) {
            Logger.getLogger(dlgMasOfertaAcademica.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(dlgMasOfertaAcademica.class.getName()).log(Level.SEVERE, null, ex);
        }
        return listaAjuste;
}

