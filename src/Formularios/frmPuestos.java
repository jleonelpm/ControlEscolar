private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
 // TODO add your handling code here:
    int res=JOptionPane.showConfirmDialog(this, "¿Desea eliminar este registro?");
    if(res==JOptionPane.YES_OPTION)
    {
    
    Puestos obj=new Puestos();
    ctrlPuestos ctrlobj=new ctrlPuestos();
         obj.setIdPuesto(Integer.parseInt(this.lblIddescripcion.getText()));
          
             
            if( ctrlobj.deletePuesto(obj)>0)
            {  this.updateTabla();
                this.setLimpiar();
                JOptionPane.showMessageDialog(null, "La Operación se Realizó de Forma Exitorsa","Aviso",JOptionPane.INFORMATION_MESSAGE);
            }
            else
                JOptionPane.showMessageDialog(null, "Los Cambios NO se Realizaron Debido a un ERROR","Aviso",JOptionPane.ERROR_MESSAGE);
    }
    
}//GEN-LAST:event_btnEliminarActionPerformed

private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
 // TODO add your handling code here:
    int res=JOptionPane.showConfirmDialog(this, "¿Desea Guardar los Cambios?");
    if(res==JOptionPane.YES_OPTION)
    {
    
    Puestos obj=new Puestos();
    ctrlPuestos ctrlobj=new ctrlPuestos();
         if( obj.setDescripcion(this.txtDescripcion.getText().trim()))
         {  int id_puesto=Integer.parseInt(this.lblIddescripcion.getText());
             obj.setIdPuesto(id_puesto);
            if( ctrlobj.setPuestos(obj)>0)
            {
                this.setLimpiar();
                this.updateTabla();
                JOptionPane.showMessageDialog(null, "Los Cambios se Guardaron de Forma Exitosa","Aviso",JOptionPane.INFORMATION_MESSAGE);
            }
            else
                JOptionPane.showMessageDialog(null, "Error en la operación","Aviso",JOptionPane.ERROR_MESSAGE);
         }
         else
         {
          JOptionPane.showMessageDialog(null, "Falta el dato: " + this.jLabel2.getText() );
         }
    }
    
}//GEN-LAST:event_btnGuardarActionPerformed

