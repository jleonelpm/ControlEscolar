    private void tablaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaMouseClicked
        // TODO add your handling code here:
        int fila = tabla.rowAtPoint(evt.getPoint());                 
        int col = tabla.columnAtPoint(evt.getPoint());
        if (col == 6) {
            docente = "";
            //JOptionPane.showMessageDialog(this, "fila = " + fila + " columna = " + col);
            frmListaPersonal1 frm = new frmListaPersonal1(this,true);
            frm.setLocationRelativeTo(frm);
            frm.setVisible(true);
            
            if (!"".equals(docente)) {
                String[] p = docente.split(",");
                tabla.setValueAt(p[0], fila, 4); //Columna Id del docente            
                tabla.setValueAt(p[1], fila, 5); //Columna nombre del docente
            }
            //JOptionPane.showMessageDialog(this, "seleccionado = " + id_docente);
            
        }
        
        
    }//GEN-LAST:event_tablaMouseClicked

