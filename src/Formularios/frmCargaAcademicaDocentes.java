    private void updateTabla(){
        try {
            String[] titulos = {"id_carga","Carrera","Grupo", "Asignatura/Módulo","id_personal","Docente",""};  
            // se utiliza la funcion
            ctrlOfertaAcademica carga = new ctrlOfertaAcademica();
            MyTableModel datos = new MyTableModel(carga.getCargaAcademicaDocentes(),titulos);
            tabla.setModel(datos);
            TableColumn columna;
            columna = tabla.getColumn("id_carga");                    
            columna.setPreferredWidth(0);
            columna.setMinWidth(0);
            columna.setMaxWidth(0);            
            columna = tabla.getColumn("Grupo");                    
            columna.setPreferredWidth(80);
            columna.setMinWidth(80);
            columna.setMaxWidth(80);
            columna = tabla.getColumn("id_personal");                    
            columna.setPreferredWidth(0);
            columna.setMinWidth(0);
            columna.setMaxWidth(0);            
            columna = tabla.getColumn("");                    
            columna.setPreferredWidth(15);
            columna.setMinWidth(15);
            columna.setMaxWidth(15);
            
            
        } catch (SQLException ex) {
            Logger.getLogger(frmCargaAcademicaDocentes.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(frmCargaAcademicaDocentes.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
  private  void guardar() throws ClassNotFoundException, SQLException{
      
      ctrlOfertaAcademica carga = new ctrlOfertaAcademica();          
      boolean result;
      result = carga.setOfertaAcademica(tabla.getModel());

      if (result){
        JOptionPane.showMessageDialog(this, "Los cambios fueron realizados exitosamente");                                  
      }else{
        JOptionPane.showMessageDialog(this, "El ciclo escolar no pudo agregarse debido a un problema");                    
      }
      
      
  }
    
