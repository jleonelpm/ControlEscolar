private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
    // TODO add your handling code here:
    int filas = this.tablaAlumnos.getSelectedRow();
    if (filas > -1) {
        Alumnos objalumn = new Alumnos();
        int id = Integer.parseInt(String.valueOf(this.tablaAlumnos.getValueAt(filas, 0)));
        objalumn.setIdAlumno(id);
        objalumn = ctrlalumno.getPersonal(objalumn, this.lista);
        Alumnos mialumno = objalumn;
        if (objalumn.getNombre() != null) {
            try {
                frmCrearKardex crearkardex = new frmCrearKardex(this, true);
                crearkardex.setAlumno(mialumno);
                crearkardex.setLocationRelativeTo(null);

                crearkardex.setVisible(true);
            } catch (SQLException ex) {
                Logger.getLogger(dlgBuscarAlumnokardex.class.getName()).log(Level.SEVERE, null, ex);
            }

        }
    } else {
        JOptionPane.showMessageDialog(this, "Dese seleccionar un Alumno");
    }
}//GEN-LAST:event_btnAgregarActionPerformed

