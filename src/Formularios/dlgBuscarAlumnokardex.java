private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
    // TODO add your handling code here:
    int filas = this.tablaAlumnos.getSelectedRow();
    if (filas > -1) {
        try {
            Alumnos objalumn = new Alumnos();
            int id = Integer.parseInt(String.valueOf(this.tablaAlumnos.getValueAt(filas, 0)));
            objalumn.setIdAlumno(id);
            objalumn = ctrlalumno.getAlumno(objalumn);
            Alumnos mialumno = objalumn;





            ctrlKardex ctrlka = new ctrlKardex();
            ArrayList<ReporteHistorialAcademico> r = ctrlka.getReporteHistorialAcademico(id);
            if (r != null) {
                if (r.size() > 0) {
                    JRDHistorialAcademicoAlumno reporte = new JRDHistorialAcademicoAlumno();
                    for (int i = 0; i < r.size(); i++) {

                        reporte.addRegistroReporte(r.get(i));

                    }
                    String ruta = System.getProperty("user.dir");
                    String archivo = "historial_academico.jrxml";//=ruta+"\\src\\Reportes\\formatoREDI.jrxml";

                    archivo = ruta + "\\src\\Reportes\\" + archivo;
                    Map pars = new HashMap();
                    pars.put("p_matricula", objalumn.getMatricula());
                    pars.put("p_nombre", objalumn.getNombre());
                    JasperDesign jasperDesign = JRXmlLoader.load(archivo); //Cargamos el reporte jrxml para generar el jasper
                    JasperReport jasper = JasperCompileManager.compileReport(jasperDesign); //Compilamos para generar el jasper
                    JasperPrint jasperprint = JasperFillManager.fillReport(jasper, pars, reporte);
                    if (jasperprint.getPages().size() > 0) //Vemos si el reporte tiene hojas (registros) sino no tiene caso abrir el visor
                    {
                        JasperViewer.viewReport(jasperprint, false);



                    } else {
                        JOptionPane.showMessageDialog(null, "NO HAY REGISTROS");
                    }

                }
            }


        } catch (JRException ex) {
            Logger.getLogger(dlgBuscarAlumnokardex.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(dlgBuscarAlumnokardex.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(dlgBuscarAlumnokardex.class.getName()).log(Level.SEVERE, null, ex);
        }

    } else {
        JOptionPane.showMessageDialog(this, "Dese seleccionar una fila");
    }



}//GEN-LAST:event_jButton2ActionPerformed

