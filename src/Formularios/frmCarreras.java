  private void guardar() throws SQLException, ClassNotFoundException{    
    
    if (validar() ) {
    
      if (JOptionPane.showConfirmDialog(this, "¿Deseas guardar los cambios?") == JOptionPane.YES_OPTION) {

        if ("".equals(lblIdCarrera.getText())) {
          c.setIdCarrera(0);      
        }else{
          c.setIdCarrera(Integer.parseInt(lblIdCarrera.getText()));            
        }

        c.setClave(txtClave.getText());
        c.setNombre(txtNombre.getText());   
        c.setEstado("A");   

        int result = carrera.setCarrera(c);

        if (result > 0) {
         updateTabla();
         limpiar();
          JOptionPane.showMessageDialog(this, "Los cambios fueron realizados con éxito");             
        }else{
          JOptionPane.showMessageDialog(this, "La carrera no pudo agregarse debido a un problema");                    
        }// fin de if (result > 0)     
      } //fin de ¿Guardar los cambios?
    } //fin de validar
  }  
  
  private void eliminar() throws SQLException{
    try {    
      if (!"".equals(lblIdCarrera.getText())) {
      if (JOptionPane.showConfirmDialog(this, "¿Deseas eliminar esta carrera?") == JOptionPane.YES_OPTION) {
        int id_carrera = Integer.parseInt(lblIdCarrera.getText());
        int result = carrera.deleteCarrera(id_carrera);
        if (result > 0) {
          updateTabla();
          limpiar();
          JOptionPane.showMessageDialog(this, "Los carrera fue eliminada con éxito");             
        }else{
          JOptionPane.showMessageDialog(this, "La carrera no pudo eliminarse debido a un problema");                    
        }
      }
      }else{
          JOptionPane.showMessageDialog(this, "Selecciona una carrera");                     
      }
    }catch (Exception e) {
      JOptionPane.showMessageDialog(this, "La carrera NO puede eliminarse debido \n a que tiene planes de estudio asociados");                          
    }
  }
  
  private void updateTabla() throws ClassNotFoundException{             

        try {
          
          modelo =  new DefaultTableModel();
          modelo = carrera.getCarreras(carrera.getCarreras(0));
          tabla.setModel(modelo);
          
          //Ocultando una columna, las tres son obligatorias
          tabla.getColumnModel().getColumn(0).setMaxWidth(0);
          tabla.getColumnModel().getColumn(0).setMinWidth(0);
          tabla.getColumnModel().getColumn(0).setPreferredWidth(0);

          //tabla.getColumnModel().getColumn(1).setMaxWidth(80);
          //tabla.getColumnModel().getColumn(2).setMaxWidth(120);

          //tabla.getColumnModel().getColumn(1).setMinWidth(120);          
         // tabla.getColumnModel().getColumn(1).setPreferredWidth(120);
          
        }catch (SQLException e){
            JOptionPane.showMessageDialog(this, "error" + e.getMessage());                    
        }
    }
  
  
  private void limpiar(){
    lblIdCarrera.setText("");
    txtClave.setText("");
    txtNombre.setText("");    
    
  }

  private void bloquear(boolean opcion){ 
    txtClave.setEditable(opcion);
    txtNombre.setText("");        
  }
  
  
  private boolean validar(){
    boolean result=false;

    if ("".equals(txtClave.getText())){
      JOptionPane.showMessageDialog(this, "Escribe la clave de la carrera");                    
      result = false;
    } else if ("".equals(txtNombre.getText())) {
      JOptionPane.showMessageDialog(this, "Escribe el nombre de la carrera");                    
      result=false;
    } else {
      result = true;
    } 

    return result;
    
  }

