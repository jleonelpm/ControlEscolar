    private void filtrar(){
        
            Planes pa = (Planes) cmbPlan.getSelectedItem();
            if (pa != null) {
                id_plan = pa.getIdPlan();
            }
        
        
        Carreras ca = (Carreras) cmbCarrera.getSelectedItem();
        if (ca != null) {
            id_carrera = ca.getIdCarrera();


        }
        
        Areas ar = (Areas) cmbArea.getSelectedItem();
        if (ar != null) {
            id_area = ar.getIdArea();
        }
        

        if (id_area == 0 && id_carrera == 0 && id_plan == 0) {
            opcion = 0; //Todas las grupos            
        } else if (id_area == 0 && id_carrera == 0 && id_plan != 0) {
            opcion = 1; //Los grupos de una generacion
        } else if (id_area == 0 && id_carrera != 0 && id_plan == 0) {
            opcion = 2; //Los grupos de una generacion y carrera            
        } else if (id_area == 0 && id_carrera != 0 && id_plan != 0) {
            opcion = 3; //Los grupos de una generacion, carrera y plan
        } else if (id_area != 0 && id_carrera != 0 && id_plan != 0) {
            opcion = 4; //Los grupos de una generacion, carrera y plan
        } else if (id_area != 0 && id_carrera == 0 && id_plan == 0) {
            opcion = 5; //Los grupos de una generacion, carrera y plan
        }

        updateTabla();
    }

