    private void guardar() {
        try {
            if (validar()) {

                if (JOptionPane.showConfirmDialog(this, "驴Deseas guardar los cambios?") == JOptionPane.YES_OPTION) {

                    if ("".equals(lblIdModulo.getText())) {
                        m.setIdModulo(0);
                    } else {
                        m.setIdModulo(Integer.parseInt(lblIdModulo.getText()));
                    }


                    m.setModulo(cmbModulo.getSelectedItem().toString());
                    m.setClave(txtClave.getText());
                    Carreras c = (Carreras) cmbCarrera.getSelectedItem();
                    m.setIdCarrera(c.getIdCarrera());
                    m.setDescripcion(txtDescripcion.getText());
                    m.setHoras(Integer.parseInt(txtHoras.getText()));

                    int result = modulo.setModulo(m);

                    if (result > 0) {
                        updateTabla();
                        limpiar();
                        JOptionPane.showMessageDialog(this, "Los cambios fueron realizados con 茅xito");
                    } else {
                        JOptionPane.showMessageDialog(this, "El m贸dulo no pudo agregarse debido a un problema");
                    }// fin de if (result > 0)     
                } //fin de 驴Guardar los cambios?
            } //fin de validar
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "error" + e.getMessage());
        }
    }

    private void eliminar() {
        try {
            if (!"".equals(lblIdModulo.getText())) {
                if (JOptionPane.showConfirmDialog(this, "驴Deseas eliminar este m贸dulo?") == JOptionPane.YES_OPTION) {

                    int id_modulo = Integer.parseInt(lblIdModulo.getText());

                    int result = modulo.deleteModulo(id_modulo);
                    if (result > 0) {
                        updateTabla();
                        limpiar();
                        JOptionPane.showMessageDialog(this, "El m贸dulo fue eliminado con 茅xito");
                    } else {
                        JOptionPane.showMessageDialog(this, "El m贸dulo no pudo eliminarse debido a un problema");
                    }
                }
            } else {
                JOptionPane.showMessageDialog(this, "Selecciona un plan de estudios");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "El plan NO puede eliminarse debido \n a que tiene asignaturas asociadas");
        }
    }

