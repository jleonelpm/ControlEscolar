    private void updateComboCarreras(int id_carrera) {
        Carreras c = new Carreras();

        try {

            DefaultComboBoxModel modeloCombo;
            modeloCombo = new DefaultComboBoxModel();
            modeloCombo.removeAllElements();
            modeloCombo = carrera.getComboCarreras(lstCarreras);
            cmbCarrera.setModel(modeloCombo);
            c.setIdCarrera(id_carrera); //Establecemos el id de la carrera
            cmbCarrera.setSelectedItem(c);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "error" + e.getMessage());
        }
    }

    private void limpiar() {
        lblIdModulo.setText("");
        updateComboCarreras(0);
        cmbModulo.setSelectedIndex(0);
        txtDescripcion.setText("");
        txtClave.setText("");
        txtHoras.setText("");
    }

    private boolean validar() {
        boolean result = false;

        Carreras ca = (Carreras) cmbCarrera.getSelectedItem();
        if (ca != null) {
            id_carrera = ca.getIdCarrera();
        }

        if (id_carrera == 0) {
            JOptionPane.showMessageDialog(this, "Selecciona una carrera");
        } else if ("".equals(this.txtClave.getText())) {
            JOptionPane.showMessageDialog(this, "Escribe la clave del m贸dulo");            
        } else if ("".equals(this.txtHoras.getText())) {
            JOptionPane.showMessageDialog(this, "Escribe las horas del m贸dulo");            
        } else if ("".equals(txtDescripcion.getText())) {
            JOptionPane.showMessageDialog(this, "Escribe la descripci贸n del m贸dulo");
        } else {
            result = true;
        }

        return result;

    }

