private void tablaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaMouseClicked
// TODO add your handling code here:
    int id_carrera;
    fila = tabla.rowAtPoint(evt.getPoint());
    if (fila > -1) {

        lblIdModulo.setText(String.valueOf(tabla.getValueAt(fila, 0)));
        
        String c = String.valueOf(tabla.getValueAt(fila, 1));
        
        if ("I".equals(c)) {
            cmbModulo.setSelectedIndex(0);
        }else if ("II".equals(c)) {
            cmbModulo.setSelectedIndex(1);            
        }else if ("III".equals(c)) {
            cmbModulo.setSelectedIndex(2);            
        }else if ("VI".equals(c)) {
            cmbModulo.setSelectedIndex(3);            
        }else if ("V".equals(c)) {
            cmbModulo.setSelectedIndex(4);            
        }else if ("VI".equals(c)) {
            cmbModulo.setSelectedIndex(5);            
        }

        txtClave.setText(String.valueOf(tabla.getValueAt(fila, 2)));
                

        id_carrera = Integer.parseInt(String.valueOf(tabla.getValueAt(fila, 3)));
        //id_carrera = String.valueOf(tabla.getValueAt(fila, 2));
        updateComboCarreras(id_carrera);

        txtDescripcion.setText(String.valueOf(tabla.getValueAt(fila, 5)));
        txtHoras.setText(String.valueOf(tabla.getValueAt(fila, 6)));        
        //txtClave.setText(String.valueOf(tabla.getValueAt(fila, 1)));             


    }
}//GEN-LAST:event_tablaMouseClicked

