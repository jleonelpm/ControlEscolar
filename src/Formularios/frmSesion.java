private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        try {
            // TODO add your handling code here:
              Usuarios usuario=new Usuarios();
               usuario.setUsuario(this.txtUsuario.getText());
               String pass=new String(this.txtPassword.getPassword());
               usuario.setPassword(pass);
               ctrlUsuarios ctrlusuarios=new ctrlUsuarios();
                
               //System.out.println(usuario);
               
               if(ctrlusuarios.getAutentificar(usuario))
               {
                   //System.out.println("Entro");
                   Personal personal=ctrlusuarios.getPersonalAutorizado();
                    usuario = ctrlusuarios.getDatosUsuario();
                   //frmprincipal.setPersonal(personal);
                   frmPrincipal frmprincipal=new frmPrincipal(personal, usuario);                 
                   frmprincipal.setExtendedState( JFrame.MAXIMIZED_BOTH);
                   
                   frmprincipal.setVisible(true);
                   
                this.dispose();
               }else{
                   JOptionPane.showMessageDialog(this, "Nombre de usuario o contraseña incorrectos","Aviso",JOptionPane.WARNING_MESSAGE);
               }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, ex.getMessage(),"Error de Base de Datos",JOptionPane.ERROR_MESSAGE);
            Logger.getLogger(frmSesion.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(rootPane, ex.getMessage(),"Error General del Sistema",JOptionPane.ERROR_MESSAGE);            
            Logger.getLogger(frmSesion.class.getName()).log(Level.SEVERE, null, ex);
        }
}//GEN-LAST:event_btnAceptarActionPerformed

